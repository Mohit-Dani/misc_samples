public class Book
{
	String bid;
	String name, author, desc;
	double price;
	Book(String bid, String name, String author, String desc, double price)
	{
		this.bid = bid;
		this.name = name;
		this.author = author;
		this.desc = desc;
		this.price = price;
	}
}
